package Loops;

public class WhileLoop {

    public static void main(String[] args) {
        System.out.println("Running While Loop.");
        

        int i = 1;
//      Syntax of while loop
        while (i > 0 && i <= 5) {
            System.out.println("Value of i is =>" + i);
            i++;
        }
    }
}
